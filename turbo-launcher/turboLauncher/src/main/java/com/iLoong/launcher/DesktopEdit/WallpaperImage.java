package com.iLoong.launcher.DesktopEdit;

import android.graphics.Bitmap;


public class WallpaperImage
{
	private String name;
	private Bitmap bit;
	
	public String getName()
	{
		return name;
	}
	
	public void setName(
			String name )
	{
		this.name = name;
	}
	
	public Bitmap getBit()
	{
		return bit;
	}
	
	public void setBit(
			Bitmap bit )
	{
		this.bit = bit;
	}
}
